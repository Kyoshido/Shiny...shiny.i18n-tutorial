mod_slider_ui <- function(id,
                          # Parameters
                          i18n
) {
  ns <- NS(id)
  tagList(
    
    ###########################################################################
    # UI ----------------------------------------------------------------------
    ###########################################################################
    
    sliderInput(ns("bins"),
                i18n$t("Number of bins:"), # you use i18n object as always
                min = 1,
                max = 50,
                value = 30
    )
    
    ###########################################################################
    # -------------------------------------------------------------------------
    ###########################################################################
  )
}

###############################################################################

mod_slider_server <- function(id
){
  moduleServer( id, function(input, output, session){
    ns <- session$ns
    
    ###########################################################################
    # Server ------------------------------------------------------------------
    ###########################################################################
    
    bins <- reactive({ input$bins })
    return(bins)
    
    ###########################################################################
    # -------------------------------------------------------------------------
    ###########################################################################
    
  })
}

