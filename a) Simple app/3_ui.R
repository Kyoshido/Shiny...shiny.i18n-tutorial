                  selected = i18n$get_key_translation())
  ),
  
  #############################################################################
  # UI ------------------------------------------------------------------------
  #############################################################################
  
  # Application title
  titlePanel(i18n$t("Hello Shiny!"), windowTitle = NULL),
  
  # Sidebar with a slider input for number of bins 
